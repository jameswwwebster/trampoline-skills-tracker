/**
 * Deletes session instances (and their bookings) that fall before their
 * template's startDate. Run with --dry-run to preview without deleting.
 *
 * Usage:
 *   node scripts/cleanup-pre-startdate-instances.js
 *   node scripts/cleanup-pre-startdate-instances.js --dry-run
 */

const { PrismaClient } = require('@prisma/client');

const prisma = new PrismaClient();
const DRY_RUN = process.argv.includes('--dry-run');

async function main() {
  if (DRY_RUN) console.log('--- DRY RUN — no changes will be made ---\n');

  // Find all templates that have a startDate set
  const templates = await prisma.sessionTemplate.findMany({
    where: { startDate: { not: null } },
    select: { id: true, dayOfWeek: true, startTime: true, startDate: true, club: { select: { name: true } } },
  });

  if (templates.length === 0) {
    console.log('No templates with a startDate found. Nothing to do.');
    return;
  }

  let totalInstances = 0;
  let totalBookings = 0;

  for (const template of templates) {
    const startDate = new Date(template.startDate);
    startDate.setHours(0, 0, 0, 0);

    // Find instances before the start date
    const instances = await prisma.sessionInstance.findMany({
      where: {
        templateId: template.id,
        date: { lt: startDate },
      },
      include: {
        bookings: { select: { id: true } },
      },
    });

    if (instances.length === 0) continue;

    const bookingIds = instances.flatMap(i => i.bookings.map(b => b.id));

    console.log(`Template ${template.startTime} (${template.club.name}), startDate: ${startDate.toDateString()}`);
    console.log(`  → ${instances.length} instance(s) to remove, ${bookingIds.length} booking(s)`);

    totalInstances += instances.length;
    totalBookings += bookingIds.length;

    if (DRY_RUN) continue;

    if (bookingIds.length > 0) {
      // Restore credits that were used on these bookings (un-apply them)
      await prisma.credit.updateMany({
        where: { usedOnBookingId: { in: bookingIds } },
        data: { usedOnBookingId: null, usedAt: null },
      });

      // Delete credits that were generated by these bookings
      await prisma.credit.deleteMany({
        where: { sourceBookingId: { in: bookingIds } },
      });

      // Delete bookings (cascades to BookingLines)
      await prisma.booking.deleteMany({
        where: { id: { in: bookingIds } },
      });
    }

    const instanceIds = instances.map(i => i.id);

    // Delete waitlist entries and attendances
    await prisma.waitlistEntry.deleteMany({ where: { sessionInstanceId: { in: instanceIds } } });
    await prisma.attendance.deleteMany({ where: { sessionInstanceId: { in: instanceIds } } });

    // Delete the instances
    await prisma.sessionInstance.deleteMany({ where: { id: { in: instanceIds } } });
  }

  console.log(`\n${DRY_RUN ? '[DRY RUN] Would remove' : 'Removed'}: ${totalInstances} instance(s), ${totalBookings} booking(s)`);
}

main()
  .catch(err => { console.error(err); process.exit(1); })
  .finally(() => prisma.$disconnect());
